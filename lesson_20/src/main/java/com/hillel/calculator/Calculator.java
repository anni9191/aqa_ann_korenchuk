package com.hillel.calculator;

public class Calculator {

    public int summ(int a, int b){
        return a + b;
    }

    public int division(int a, int b){
       return a / b;
    }

    public int multiplication(int a, int b){
        return a * b;
    }

    public int substact(int a, int b){
        return a - b;
    }
}
